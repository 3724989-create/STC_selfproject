// *************************** 例程硬件连接说明 ***************************
// 使用逐飞科技调试下载器连接
//      直接将下载器正确连接在核心板的调试下载接口即可
//
// 使用 USB-TTL 模块连接
//      模块管脚            单片机管脚
//      USB-TTL-RX          查看 zf_common_debug.h 文件中 DEBUG_UART_TX_PIN 宏定义的引脚 默认 B10
//      USB-TTL-TX          查看 zf_common_debug.h 文件中 DEBUG_UART_RX_PIN 宏定义的引脚 默认 B11
//      USB-TTL-GND         核心板电源地 GND
//      USB-TTL-3V3         核心板 3V3 电源
//
// 接入 DL1A 模块
//      模块管脚            单片机管脚
//      SCL                 查看 zf_device_dl1a.h 中 DL1A_SCL_PIN  宏定义 默认 P16
//      SDA                 查看 zf_device_dl1a.h 中 DL1A_SDA_PIN  宏定义 默认 P14
//      XS                  查看 zf_device_dl1a.h 中 DL1A_XS_PIN   宏定义 默认 不接
//      VCC                 5V 电源
//      GND                 电源地

// *************************** 例程测试说明 ***************************
// 1.核心板烧录完成本例程
//
// 2.在断电情况下，单独使用 type-c接口 或者 USB-TTL 模块，在断电情况下完成连接，并连接好 DL1B 模块
//      ( 如果使用配套的学习主板测试，则直接将 DL1B 模块接到对应的接口位置 )
//
// 3.将 type-c接口 或者 USB-TTL 模块连接电脑，完成上电
//      ( 如果使用配套的学习主板测试，则需要直接使用电池供电，2S锂电池/蓝电池电压不应低于7.8V，3S锂电池电压不应低于11.3V )
//
// 4.可以在串口助手上看到如下串口信息：
//      DL1A distance data: ...
//      DL1A distance data: ...
//
// 如果发现现象与说明严重不符 请参照本文件最下方 例程常见问题说明 进行排查


// **************************** 代码区域 ****************************
#define LED1                        (IO_P52 )
#define PIT                         (TIM0_PIT )                                 // 使用的周期中断编号 如果修改 需要同步对应修改周期中断编号与 isr.c 中的调用

uint16 pit_ms_count = 0;

void pit_handler (void); 
	
void main()
{
    clock_init(SYSTEM_CLOCK_30M);
	debug_init();

    // 此处编写用户代码 例如外设初始化代码等
    gpio_init(LED1, GPO, GPIO_HIGH, GPO_PUSH_PULL);                             // 初始化 LED1 输出 默认高电平 推挽输出模式

    while(1)
    {
        if(dl1a_init())
        {
            printf("\r\nDL1A init error.");                                     // DL1A 初始化失败
        }
        else
        {
            break;
        }
        gpio_toggle_level(LED1);                                                // 翻转 LED 引脚输出电平 控制 LED 亮灭 初始化出错这个灯会闪的很慢
    }
    pit_ms_init(PIT, 5);
	
	// 设置定时器周期中断回调函数
	tim0_irq_handler = pit_handler;
	
    // 此处编写用户代码 例如外设初始化代码等

    while(1)
    {
        // 此处编写需要循环执行的代码
        system_delay_ms(500);
        printf("\r\nDL1A distance data: %5d", dl1a_distance_mm);
        gpio_toggle_level(LED1);                                                // 翻转 LED 引脚输出电平 控制 LED 亮灭
        // 此处编写需要循环执行的代码
    }
}
//-------------------------------------------------------------------------------------------------------------------
// 函数简介     PIT 的中断处理函数 这个函数将在 PIT 对应的定时器中断调用 详见 isr.c
// 参数说明     void
// 返回参数     void
// 使用示例     pit_handler();
//-------------------------------------------------------------------------------------------------------------------
void pit_handler (void)
{
    if(0 == pit_ms_count % 40)                                                  // 每 40ms 获取一次测距信息 周期 40 ms 频率 25Hz
    {
        dl1a_get_distance();                                                    // 测距调用频率不应高于 30Hz 周期不应低于 33.33ms
    }
    pit_ms_count = (pit_ms_count == 995) ? (0) : (pit_ms_count + 5);            // 1000ms 周期计数
}
